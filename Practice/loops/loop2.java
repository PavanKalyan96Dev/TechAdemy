package com.loops;

public class loop2 {

	public static void main(String[] args) {
		
		int n=6;
		int n2=7;
		for(int i=1;i<=10;i++)
		{
			System.out.printf("%d*%d=%d",n,i,n*i).println();
		}
		System.out.println();
		for(int i=1;i<=10;i++)
		{
			System.out.printf("%d*%d=%d",n2,i,n2*i).println();
		}
	}

}

